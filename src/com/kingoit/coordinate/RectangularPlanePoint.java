package com.kingoit.coordinate;

/**
 * 空间直角坐标系中的点
 * @author Zhou.Zeyong
 *
 */
public class RectangularPlanePoint {
	private double x;
	
	private double y;
	
	private double z;
	
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	public double getZ() {
		return z;
	}
	public void setZ(double z) {
		this.z = z;
	}
}
